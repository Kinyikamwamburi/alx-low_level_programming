#include <string.h>

/**
 * _strcat - a function that concatenates two strings
 *
 * @dest: string to be concatenated to src
 * @src: string to be concatenated to dest
 *
 * Return: dest the concatenated string
 */
char *_strcat(char *dest, char *src)
{
	strcat(dest, src);

		return (dest);

}
